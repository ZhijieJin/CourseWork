; Print a null-terminated string to the monitor using a large font
; Information of the string is stored starting from x5000
; String end with NULL
;
; Program store row informaiton of each letter starting x6000
; Program reload the information starting x6000 when printing
;
; Register used:
; R0 used to print data on screen (doesn't need to be initialized)
; R1 used as row pointers
;  & used to store the starting row in ASCII for each letter
; R2 used to store letter address
;  & used as a row pointer when printing symbles
; R3 used to store letter information
; R4 used to store letter representation for each row on screen
; R5 used as a 16 number counter and row counter
; R6 used as column counter
; R7 used to store information to designated location
;
		; Program starts here
		.ORIG x3000		; Start program at location x3000
		LD R2, LETTER 	 	; Load letter address to R2
		LD R7, STORE		; Load location x6000 to R7
		;
		; Get first row thar represents each letter and 
		; store them in a new location in memory
FIRST_ROWS	LEA R1, FONT_DATA	; Load first ascii address
		LDR R3, R2, #0		; Load letter to R3
		BRz STORE_NULL		; Check null line
		;
		; 16 number counter
		AND R5, R5, #0		; Clear counter to be used
		ADD R5, R5, #8		
		ADD R5, R5, #8		; Initial counter to count 16 times
		;
		; Loop to find first row that represents the character
LOOP1		ADD R1, R1, R3		; Load content in location R3 to R0
		ADD R5, R5, #-1		; Decrement the 16-counter
		BRp LOOP1		; Loop to get first row to represent letters
		STR R1, R7, #0		; Store first rows of letter to new locations
		ADD R2, R2, #1		; Get the location of next letter
		ADD R7, R7, #1		; Get next location to
					; Store next first row of letters
		BRnzp FIRST_ROWS	; Always brach back to get next letter
		;
		; Setting null address
STORE_NULL	AND R1, R1, #0
		STR R1, R7, #0			
		;

		; In the following part, print symbles through 3 loops
		; Register specification in this part:
		; R1 is location store the starting row of each letter
		; R2 is the row pointer of each letter
		; R3 counter from 0 to the length of the string
		; R4 store the row information of each letter
		; that needs to be printed on the screen
		;
		; Re-initialize R5 as row counter 
		AND R5, R5, #0		; Clear the row counter
		ADD R5, R5, #8
		ADD R5, R5, #8		; Initial R5 to count 16 times
		AND R3, R3, #0		; Clear R3 to count
		;
		; Loop through 16 lines that represent the character
LOOP_R		LD R1, STORE		; Load content in STORE to R1
					; R1 contains location of 
					; letter's row information x6000
NEXT_LOOP	AND R6, R6, #0 		; Clear R6 as a column counter
		ADD R6, R6, #8		; Initial R6 to count 8 times
		LDR R2, R1, #0 		; Load bits in address R1 to R2
					; R2 stores row address (row pointer)
		ADD R2, R2, #0  	; Check if next location is null
		BRz NEXT_LINE		; Skip to a new line if null
		ADD R2, R2, R3		; Increment row pointer
		LDR R4, R2, #0		; R4 stores infor need to represent 
					; Letter for each row
		;
		; Loop through each columns in each row
LOOP_C		ADD R4, R4, #0		; Check first bit (negativity)
		BRn NEXT		; BR if negative
		LDI R0, NULL		; Print symbols that is the shadow
		BRnzp OUTPUT		; Always print on screen
NEXT		LDI R0, CHAR		; Print symbols that represent letters
OUTPUT		OUT			; Print output on screen
		ADD R4, R4, R4		; Peft shift R4 by one bit
		ADD R6, R6, #-1		; Decrement column counter
		BRp LOOP_C
		;
		ADD R1, R1, #1		; Next letter
		BRnzp NEXT_LOOP
		;
NEXT_LINE	LD R0, NEWLINE		; Start printing at a new line
		OUT			; Print the new line on the screen
		ADD R3, R3, #1		; Increament count to move row pointer
		ADD R5, R5, #-1		; Decrement row counter
		BRp LOOP_R
		;
		; Halt program
DONE		HALT
		;
; Pseudo Ops
NEWLINE	.FILL   xA
NULL    .FILL   x5000
CHAR    .FILL   x5001
LETTER	.FILL   x5002
STORE	.FILL   x6000



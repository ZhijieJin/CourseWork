; program to print large font of letter using the information given
;
; Registers used:
; R0 used to print on screen
; R1 used as register to store shadows symble
; R2 used as register to store character symble
; R3 used as register to store letters to be larged
; R4 used to store information for each columns
; R5 used as a 16 interation counter
; R6 used as a 8 interation counter
; R7 used as a row pointer
;
; program starts here
	.ORIG x3000 		; Start at x3000
	LEA R7, FONT_DATA 	; load first ascii address
	LDI R3, LETTER  	; Load character information
    
	; 16-counter
	AND R5, R5, #0		; clear counter to be used
	ADD R5, R5, #8		
	ADD R5, R5, #8		; initial counter to count 16 times

	; loop to find first row that represents the character
LOOP1	ADD R7, R7, R3		; load content in location R3 to R0
	ADD R5, R5, #-1		; decrement the 16-counter
	BRp LOOP1
	ST R7, STORE		; store R7 to an address

	; re-initialize R5 as row counter
	AND R5, R5, #0		; clear the row counter
	ADD R5, R5, #8
	ADD R5, R5, #8		; initial R5 to count 16 times

	; loop through 16 lines that represent the character
LOOP_R	AND R6, R6, #0 		; clear R6 as a column counter
	ADD R6, R6, #8		; initial R6 to count 8 times
	LDR R4, R7, #0 		; load bits in address R7 to R4

	; loop through each columns in each row
LOOP_C	ADD R4, R4, #0		; for branching
	BRn NEXT
	LDI R0, NULL		; print symbols that is the shadow
	BRnzp OUTPUT
NEXT	LDI R0, CHAR		; print symbols that represent letters
OUTPUT	OUT			; print output on screen
	ADD R4, R4, R4		; left shift R4 by one bit
	ADD R6, R6, #-1		; decrement column counter
	BRp LOOP_C

	LD R0, NEWLINE		; start printing at a new line
	OUT			; print the new line on the screen
	LD R7, STORE
	ADD R7, R7, #1		; increment row pointer R7
	ST R7, STORE
	ADD R5, R5, #-1		; decrement row counter
	BRp LOOP_R

	; halt program
DONE	HALT

NEWLINE	.FILL   xA
NULL    .FILL   x5000
CHAR    .FILL   x5001
LETTER	.FILL   x5002
STORE	.FILL   x0000

